module.exports = {
    add: function (numberA, numberB) {
        return numberA + numberB;
    }, 
    minus: function (numberA, numberB) {
        return numberA - numberB
    },
    multiply: function (numberA, numberB) {
        return numberA * numberB
    },
    divide: function (numberA, numberB) {
        return numberA / numberB
    }
}